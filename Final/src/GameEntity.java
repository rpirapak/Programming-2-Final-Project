import java.awt.*;

public abstract class GameEntity {
    protected int x;
    protected int y;
    protected Image image;

    public GameEntity(int x, int y, Image image) {
        this.x = x;
        this.y = y;
        this.image = image;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public abstract void render(Graphics g, int size);
}
